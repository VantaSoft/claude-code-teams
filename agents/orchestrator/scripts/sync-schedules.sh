#!/bin/bash
# Scans ~/agents/*/schedules/*.md across all agents, extracts cron from YAML
# frontmatter, and writes a MANAGED block to the user crontab.
#
# Preserves any crontab entries outside the MANAGED block.
# On first run, appends the block; on subsequent runs, replaces it.

set -e

AGENTS_DIR="${AGENTS_DIR:-$HOME/agents}"
BEGIN_MARKER="# BEGIN MANAGED: agent-schedules"
END_MARKER="# END MANAGED: agent-schedules"

# Collect schedule entries
new_block="$BEGIN_MARKER"
new_block+=$'\n'"# Generated by sync-schedules.sh — do not edit by hand"
new_block+=$'\n'"# Edit schedule files in ~/agents/<agent>/schedules/*.md and re-run the script"

found_count=0
for agent_dir in "$AGENTS_DIR"/*/; do
  agent=$(basename "$agent_dir")
  schedules_dir="$agent_dir/schedules"
  [ -d "$schedules_dir" ] || continue

  for schedule_file in "$schedules_dir"/*.md; do
    [ -f "$schedule_file" ] || continue

    # Extract cron from YAML frontmatter (line starting with "cron:" between the first pair of "---")
    cron=$(awk '
      /^---[[:space:]]*$/ { in_fm = !in_fm; next }
      in_fm && /^cron:/ {
        sub(/^cron:[[:space:]]*/, "")
        gsub(/^["\x27]|["\x27]$/, "")
        print
        exit
      }
    ' "$schedule_file")

    if [ -z "$cron" ]; then
      echo "Warning: no cron frontmatter found in $schedule_file, skipping" >&2
      continue
    fi

    abs_path=$(readlink -f "$schedule_file")
    new_block+=$'\n'"$cron /usr/bin/tmux send-keys -t $agent 'Read $abs_path and execute tasks defined in it.' Enter"
    found_count=$((found_count + 1))
  done
done

new_block+=$'\n'"$END_MARKER"

# Get current crontab, stripping any existing MANAGED block
current=$(crontab -l 2>/dev/null || true)

# Remove old managed block if present (between markers, inclusive)
stripped=$(echo "$current" | awk -v begin="$BEGIN_MARKER" -v end="$END_MARKER" '
  $0 == begin { in_block = 1; next }
  $0 == end { in_block = 0; next }
  !in_block { print }
')

# Compose new crontab: stripped + blank line (if any stripped content) + new block
if [ -n "$stripped" ]; then
  new_crontab=$(printf '%s\n\n%s\n' "$stripped" "$new_block")
else
  new_crontab=$(printf '%s\n' "$new_block")
fi

echo "$new_crontab" | crontab -

echo "Synced $found_count schedule(s) to crontab"
